package day01_javayaGiriş;

public class C02_Variables {
    public static void main(String[] args) {

        /*
        Java, RunTime programdır.
        Yani biz yeşil tuşa bastığımızda
        her şey sıfırdan başlar

        Class ın sonuna kadar tüm kodlar
        yukarıdan aşağı çalıştırılır

        Class bittiğinde her şey SIFIRLANIR
         */

        int bakiye=0;

        bakiye=200;
        System.out.println(bakiye); // 200


        bakiye=500;


        bakiye=300;
        System.out.println(bakiye); //300
    }
}
